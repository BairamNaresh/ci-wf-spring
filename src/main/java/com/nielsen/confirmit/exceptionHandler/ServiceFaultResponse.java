package com.nielsen.confirmit.exceptionHandler;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "ServiceFaultResponse", propOrder = {
        "code",
        "description"
})
public class ServiceFaultResponse {

    private String code;
    private String description;

    public ServiceFaultResponse() {
    }

    public ServiceFaultResponse(String code, String description) {
        this.code = code;
        this.description = description;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}